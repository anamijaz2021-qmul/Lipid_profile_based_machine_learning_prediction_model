# Re-define bootstrap_roc_auc_ci function for self-containment and improved logic
def bootstrap_roc_auc_ci(y_true, y_proba, n_bootstraps=1000, alpha=0.05):
    bootstrapped_scores = []
    n_samples = len(y_true)
    # Ensure y_true is numeric (0/1) for internal use
    y_true_numeric_local = y_true.map({'Cases': 1, 'Controls': 0}) if y_true.dtype == 'object' else y_true

    for i in range(n_bootstraps):
        # Stratified sampling for bootstrap to better handle imbalance and small datasets
        unique_classes = np.unique(y_true_numeric_local)
        if len(unique_classes) < 2:
            # Cannot perform ROC AUC if only one class is present, even in original data
            # This case should ideally be handled before calling CI function
            return np.nan, np.nan

        # Create stratified sample indices
        bootstrap_indices = []
        for cls in unique_classes:
            cls_indices = np.where(y_true_numeric_local == cls)[0]
            bootstrap_indices.extend(np.random.choice(cls_indices, size=len(cls_indices), replace=True))

        y_true_bootstrap = y_true_numeric_local.iloc[bootstrap_indices]
        y_proba_bootstrap = y_proba[bootstrap_indices]

        # Check if both classes are present in the bootstrap sample
        if len(np.unique(y_true_bootstrap)) < 2:
            # Skip this bootstrap sample if only one class is present
            continue

        score = roc_auc_score(y_true_bootstrap, y_proba_bootstrap)
        bootstrapped_scores.append(score)

    # If no valid bootstrap scores were collected, return NaN or handle appropriately
    if not bootstrapped_scores:
        return np.nan, np.nan

    # Calculate the confidence interval
    lower_bound = np.percentile(bootstrapped_scores, (alpha / 2) * 100)
    upper_bound = np.percentile(bootstrapped_scores, (1 - alpha / 2) * 100)

    return lower_bound, upper_bound

